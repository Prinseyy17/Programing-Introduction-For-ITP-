# Counting up from 0 to 50
print("Count up from 0 to 50:")
for i in range(0, 51):
    print(i, end=" ")
    
# Counting down from 50 to 0
print("\nCount down from 50 to 0:")
for i in range(50, -1, -1):
    print(i, end=" ")

# Counting up from 30 to 50
print("\nCount up from 30 to 50:")
for i in range(30, 51):
    print(i, end=" ")

# Counting down from 50 to 10 in steps of 2
print("\nCount down from 50 to 10 in steps of 2:")
for i in range(50, 9, -2):
    print(i, end=" ")

# Counting up from 100 to 200 in increments of 5
print("\nCount up from 100 to 200 in increments of 5:")
for i in range(100, 201, 5):
    print(i, end=" ")
